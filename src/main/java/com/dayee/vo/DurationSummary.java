package com.dayee.vo;


public class DurationSummary {

    private Integer useTime;
    
    private Integer remainingTime;
    
    private Integer countTime;
    
    private Double fileSize;
    
    private Double useFileSize;
    
    private Double rechargeMoney;

    public Integer getUseTime() {
    
        return useTime;
    }

    public void setUseTime(Integer useTime) {
    
        this.useTime = useTime;
    }

    
    public Integer getRemainingTime() {
    
        return remainingTime;
    }

    
    public void setRemainingTime(Integer remainingTime) {
    
        this.remainingTime = remainingTime;
    }

    public Integer getCountTime() {
    
        return countTime;
    }

    public void setCountTime(Integer countTime) {
    
        this.countTime = countTime;
    }

    
    public Double getFileSize() {
    
        return fileSize;
    }

    
    public void setFileSize(Double fileSize) {
    
        this.fileSize = fileSize;
    }

    
    public Double getUseFileSize() {
    
        return useFileSize;
    }

    
    public void setUseFileSize(Double useFileSize) {
    
        this.useFileSize = useFileSize;
    }

    
    public Double getRechargeMoney() {
    
        return rechargeMoney;
    }

    public void setRechargeMoney(Double rechargeMoney) {
    
        this.rechargeMoney = rechargeMoney;
    }
}
